#include <sys/types.h>

unsigned char	get_shifted(unsigned char c)
{
	const unsigned char	car[21] = {'`', '1', '2', '3', '4', '5', '6', '7',
		'8', '9', '0', '-', '=', '[', ']', '\\', ';', '\'', ',', '.', '/'};
	const unsigned char	s_car[21] = {'~', '!', '@', '#', '$', '%', '^', '&',
		'*', '(', ')', '_', '+', '{', '}', '|', ':', '\"', '<', '>', '?'};
	int8_t	i;

	if ('a' <= c && c <= 'z')
		return (c - 32);
	i = -1;
	while (++i < 21)
	{
		if (c == car[i])
			return (s_car[i]);
	}
	return (c);
}
